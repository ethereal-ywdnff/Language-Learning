public class NumberSystem {
    public static void main(String [] args) {
        //binary system
        int b = 0b1010;
        //octonary system
        int o = 01010;
        //decimalism
        int d = 1010;
        //hexadecimal
        int h = 0x1010;

        System.out.println(b);
        System.out.println(o);
        System.out.println(d);
        System.out.println(h);
        System.out.println("");

        int x = 1 >>> 2;
        System.out.println(x);
    }
}
